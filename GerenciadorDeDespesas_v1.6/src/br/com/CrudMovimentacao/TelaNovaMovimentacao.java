    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        int erro = 0;
        
        Movimentacao movimentacao = new Movimentacao();
        
        if (jRadioButtonDespesa.isSelected()){
            movimentacao.setTipoMovimentacao("Despesa");
        } else {
            movimentacao.setTipoMovimentacao("Receita");
        }
        
        if (radioFixaNovaMovimentacao.isSelected()){
            movimentacao.setMovimentacaoFixa("Fixa");
        } else {
            movimentacao.setMovimentacaoFixa("Não Fixa");
        }
        
        
        if (jRadioButtonReceita.isSelected()) {
            movimentacao.setStatusDespesa(" ");
        }
        if (jRadioButtonDespesa.isSelected() && jRadioButtonStatusPago.isSelected()) {
            movimentacao.setStatusDespesa("Pago");
        }
        if (jRadioButtonDespesa.isSelected() && !jRadioButtonStatusPago.isSelected()) {
            movimentacao.setStatusDespesa("Não Pago");
        }
        if (radioFixaNovaMovimentacao.isSelected()) {
            movimentacao.setNumeroDeParcelas("999999999");
        } else {
            movimentacao.setNumeroDeParcelas(jFormattedTextFieldNumeroDeParcelas.getText());
        }
        
        movimentacao.setMovimentacaoEnabled("true");

        movimentacao.setNomeMovimentacao(textFieldNomeDaMovimentacao.getText());
        movimentacao.setProprietarioDaMovimentacao(jComboBoxNomePessoa.getSelectedItem().toString());
        movimentacao.setDataMovimentacao(formattedTextFieldData.getText());
        movimentacao.setValorMovimentacao(textFieldValor.getText().replace(',', '.'));

        ServicoMovimentacao servicoMovimentacao = new ServicoMovimentacao();
        boolean nomeDaMovimentacao = servicoMovimentacao.verificarNomeMovementacao(movimentacao.getNomeMovimentacao());
        boolean dataMovimentacao = servicoMovimentacao.verificarDataMovimentacao(movimentacao.getDataMovimentacao());
        boolean numeroDeParcelas = servicoMovimentacao.verificarNumeroDeParcelas(movimentacao.getNumeroDeParcelas());
        boolean valorMovimentacao = servicoMovimentacao.verificarValorMovimentacao(movimentacao.getValorMovimentacao());

        if (nomeDaMovimentacao == false) {
            textFieldNomeDaMovimentacao.setBackground(Color.PINK);
            erro++;
        } else {
            textFieldNomeDaMovimentacao.setBackground(Color.WHITE);
        }
        if (dataMovimentacao == false) {
            formattedTextFieldData.setBackground(Color.PINK);
            erro++;
        } else {
            formattedTextFieldData.setBackground(Color.WHITE);
        }
        if (valorMovimentacao == false) {
            textFieldValor.setBackground(Color.PINK);
            erro++;
        } else {
            textFieldValor.setBackground(Color.WHITE);
        }
        if (numeroDeParcelas == false && !radioFixaNovaMovimentacao.isSelected()) {
            jFormattedTextFieldNumeroDeParcelas.setBackground(Color.PINK);
            erro++;
        } else {
            jFormattedTextFieldNumeroDeParcelas.setBackground(Color.WHITE);
        }

        if (erro == 0 && jRadioButtonDespesa.isSelected()) {
            JOptionPane.showMessageDialog(this, "Despesa Cadastrada!");
            this.dispose();
        }
        if (erro == 0 && jRadioButtonReceita.isSelected()) {
            JOptionPane.showMessageDialog(this, "Receita Cadastrada!");
            this.dispose();
        }
        if (erro == 0 && !radioFixaNovaMovimentacao.isSelected()){
            try {
                servicoMovimentacao.parcelarMovimentacao(movimentacao);
            } catch (DataSourceException ex) {
                Logger.getLogger(TelaNovaMovimentacao.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(TelaNovaMovimentacao.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (erro == 0 && radioFixaNovaMovimentacao.isSelected()){
            try {
                servicoMovimentacao.movimentacaoFixa(movimentacao);
            } catch (Exception ex) {
                Logger.getLogger(TelaNovaMovimentacao.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_botaoSalvarActionPerformed

